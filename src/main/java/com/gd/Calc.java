package com.gd;

public class Calc {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int minus(int a, int b) {
        return a - b;
    }

    public static int onePlusTwo() {
        return 1 + 2;
    }

    public static int onePlusThree() {
        return 2 + 2;
    }
    public static int onePlusEight() {
        return 1 + 8;
    }
}
